package com.derpgroup.astrobot.util.launchlibrary;

public class LaunchLibraryResponse {

  private int count;
  private int offset;
  private int total;
  
  public int getCount() {
    return count;
  }
  
  public void setCount(int count) {
    this.count = count;
  }
  
  public int getOffset() {
    return offset;
  }
  
  public void setOffset(int offset) {
    this.offset = offset;
  }
  
  public int getTotal() {
    return total;
  }
  
  public void setTotal(int total) {
    this.total = total;
  }
}
